# Create a string
string = "My Hobbies Singing"
print("String:", string)

# Concatenate strings
additional_text = " Developer"
concatenated_string = string + additional_text
print("Concatenated String:", concatenated_string)

# Access substring
substring_start = 0
substring_end = 5
substring = string[substring_start:substring_end]
print("Substring:", substring)
